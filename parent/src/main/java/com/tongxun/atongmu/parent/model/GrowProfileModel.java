public class GrowProfileModel {
    private String title;
    private String name;
    private String sex;
    private String age;
    private String date;
    private String imgpath;
    private String growthurl;
    private String growthshareurl;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getImgpath() {
        return imgpath;
    }

    public void setImgpath(String imgpath) {
        this.imgpath = imgpath;
    }

    public String getGrowthurl() {
        return growthurl;
    }

    public void setGrowthurl(String growthurl) {
        this.growthurl = growthurl;
    }

    public String getGrowthshareurl() {
        return growthshareurl;
    }

    public void setGrowthshareurl(String growthshareurl) {
        this.growthshareurl = growthshareurl;
    }
}
